class Solution {
    public int search(int[] nums, int target) {
        int l = 0 , r = nums.length - 1;
        while(l < r){
            int m = (l + r) / 2;
            if(nums[m] > nums[r]){
                l = m + 1;
            } else { 
                r = m;
            }
        }

        int pivot = l;
        l = 0;
        r = nums.length - 1;

        if(target >= nums[pivot] && target <= nums[r]){
            l = pivot;
        } else {
            r = pivot - 1;
        }

        while(l <= r){
            int m = (l + r)/ 2;
            if(nums[m] > target){
                r = m - 1;
            } else if(nums[m] < target){
                l = m + 1;
            } else {
                return m;
            }
        }

        return -1;
    }
}